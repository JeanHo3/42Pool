void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int nb;

	nb = 0;
	while (nb++ < argc - 1)
	{
		ft_putstr(argv[nb]);
		ft_putchar('\n');
	}
	return (0);
}
