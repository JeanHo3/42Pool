void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		main(int argc, char **argv)
{
	while (argc-- > 1)
	{
		ft_putstr(argv[argc]);
		ft_putchar('\n');
	}
	return (0);
}
