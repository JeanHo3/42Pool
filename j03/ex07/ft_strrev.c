char	*ft_strrev(char *str)
{
	int a;
	int i;
	int n;
	char c;
	
	i = 0;
	n = 0;
	while(str[n] != '\0')
	{
		n++;
	}
	a = n;
	while (i < a - 1)
	{
		c = str[i];
		str[i] = str[a-1];
		str[a-1] = c;
		a--;
		i++;
	}
	return (str);
}
